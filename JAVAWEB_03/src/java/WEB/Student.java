package WEB;

/**
 *
 * @author DUC
 */
public class Student {
    private String name;
    private String khoa;
    private String lop;

    public Student() {
    }

    public Student(String name, String khoa, String lop) {
        this.name = name;
        this.khoa = khoa;
        this.lop = lop;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getKhoa() {
        return khoa;
    }

    public void setKhoa(String khoa) {
        this.khoa = khoa;
    }

    public String getLop() {
        return lop;
    }

    public void setLop(String lop) {
        this.lop = lop;
    }
    
}
